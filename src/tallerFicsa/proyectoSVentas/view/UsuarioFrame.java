    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            if (validarCamposVacios()) {
                Usuario usuario = new Usuario();
                usuario.setNombre(txtNombre.getText());
                usuario.setTipoDocumento(cboTipDoc.getSelectedItem().toString());
                usuario.setNumDocumento(txtDocumento.getText());
                usuario.setDireccion(txtDireccion.getText());
                usuario.setTelefono(txtTelefono.getText());
                usuario.setEmail(txtCorreo.getText());
                usuario.setCargo(cboCargo.getSelectedItem().toString());
                usuario.setCondicion(chkEstado.isSelected() ? 1 : 0);
                usuario.setLogin(txtUsuario.getText());
                usuario.setClave(convertirSHA256(txtClave.getText()));
                int rpta = 0;
                switch (accion) {
                    case "Agregar":
                    usobj.create(usuario);
                    rpta = 1;
                    break;
                    case "Editar":
                    usuario.setIdusuario(Integer.parseInt(txtId.getText()));
                    //                        usuario.setUsuarioPermisoList(usobj.findUsuario(Integer.parseInt(txtId.getText())).getUsuarioPermisoList());
                    usuario.setVentaList(usobj.findUsuario(Integer.parseInt(txtId.getText())).getVentaList());
                    usobj.edit(usuario);
                    rpta = 1;
                    break;
                    default:
                    JOptionPane.showConfirmDialog(null, "No se encontro la accion", "Advertencia", JOptionPane.WARNING_MESSAGE);
                }
                if (rpta == 1) {
                    JOptionPane.showMessageDialog(null, "Operacion realizada correctamente", "Informacion", JOptionPane.INFORMATION_MESSAGE);
                    listarUsuarios();
                    disableForm();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Por favor no dejar campos vacios", "Advertencia", JOptionPane.WARNING_MESSAGE);

            }

        } catch (Exception e) {
            JOptionPane.showConfirmDialog(null, "Error en el sistema", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

