    private void listarUsuarios() {
        model = (DefaultTableModel) tblDatos.getModel();
        if (model.getRowCount() > 0) {
            model.setNumRows(0);
        }
        Object[] obj = new Object[12];
        try {
            listaUsuarios = usobj.findUsuarioEntities();
            for (int i = 0; i < listaUsuarios.size(); i++) {
                usuario = (Usuario) listaUsuarios.get(i);
                obj[0] = usuario.getIdusuario();
                obj[1] = usuario.getNombre();
                obj[2] = usuario.getTipoDocumento();
                obj[3] = usuario.getNumDocumento();
                obj[4] = usuario.getDireccion();
                obj[5] = usuario.getTelefono();
                obj[6] = usuario.getEmail();
                obj[7] = usuario.getLogin();
                obj[8] = usuario.getClave();
                obj[9] = usuario.getCondicion() == 1 ? "Activo" : "Inactivo";
                model.addRow(obj);
            }
            tblDatos.setModel(model);
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(null, "Error en el sistema", "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }

