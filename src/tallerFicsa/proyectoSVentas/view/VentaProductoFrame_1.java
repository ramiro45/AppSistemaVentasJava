    public void actualizarStock() {
        try {
            ArticuloJpaController articuloctrl = new ArticuloJpaController();

            for (int i = 0; i < modelo.getRowCount(); i++) {
                Articulo art = new Articulo();
                idArticulo = Integer.parseInt(tblDatos.getValueAt(i, 1).toString());
                int cant = Integer.parseInt(tblDatos.getValueAt(i, 3).toString());

                art = articuloctrl.findArticulo(idArticulo);

                int cantActual = art.getStock() - cant;

                art.setIdarticulo(idArticulo);
                art.setStock(cantActual);
                articuloctrl.edit(art);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "No se actualizo el stock- Error: " + e.getMessage(), "Advertencia", JOptionPane.WARNING_MESSAGE);

        }

    }

